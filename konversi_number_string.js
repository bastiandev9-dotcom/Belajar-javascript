//parseInt
let harga = "42000.90";
let new_harga = parseInt(harga);
console.log(`harga string: "${harga}", harga number: ${new_harga}`)

//parseFloat
let a = "42000.90";
let b = parseFloat(harga);
console.log(`harga string: "${a}", harga number: ${b}`)

//toString
let c = 450;
let d = c.toString()
